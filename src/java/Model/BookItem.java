package Model;

/**
 *
 * @author Admin
 */
public class BookItem {
    private int id;
    private String name;
    private String type;
    private float price;
    private String brand;

    public BookItem() {
    }
    
    

    public BookItem(int id, String name, String type, float price, String brand) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.price = price;
        this.brand = brand;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }
    
 
    
    
}
