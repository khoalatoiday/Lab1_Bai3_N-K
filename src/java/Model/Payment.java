package Model;

/**
 *
 * @author Admin
 */
public class Payment {
    private int id;
    private String type;
    private int discount;
   

    public Payment(int id, String type,  int discount) {
        this.id = id;
        this.type = type;
       
        this.discount = discount;
        
    }

    public Payment() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getDiscount() {
        return discount;
    }

    public void setDiscount(int discount) {
        this.discount = discount;
    }

    
    
}
